package com.cap.firstspringbootapp.controller;

/*
 * this is customer controller class
 */
public class CutomerController {

	int mul(int a, int b) {

		return a * b;
	}

	int div(int a, int b) {

		return a / b;
	}

	int add(int a, int b) {

		return a + b;
	}

	int sub(int a, int b) {

		return a - b;
	}

	int sub(int a, int b, int c) {

		return a - b - c;
	}
}
